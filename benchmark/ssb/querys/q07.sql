SELECT
    C_NATION,
    S_NATION,
    D_YEAR,
    sum(LO_REVENUE) AS REVENUE
FROM
    customer
JOIN
    lineorder ON LO_CUSTKEY = C_CUSTKEY
JOIN
    supplier ON LO_SUPPKEY = S_SUPPKEY
JOIN
    date ON LO_ORDERDATE = D_DATEKEY
WHERE
    C_REGION = 'ASIA'
    AND S_REGION = 'ASIA'
    AND D_YEAR BETWEEN 1992 AND 1997
GROUP BY
    C_NATION,
    S_NATION,
    D_YEAR
ORDER BY
    D_YEAR ASC,
    REVENUE DESC;
